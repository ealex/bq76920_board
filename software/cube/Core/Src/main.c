/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	uint32_t button_debounce;


  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */

  LL_APB2_GRP1_EnableClock(LL_APB2_GRP1_PERIPH_SYSCFG);
  LL_APB1_GRP1_EnableClock(LL_APB1_GRP1_PERIPH_PWR);

  /* System interrupt init*/

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */

  afeConfig.enabled_cells=0b00010111;
  // current limit settings
  afeConfig.range = threshold_upper;
  afeConfig.sc_delay = scd_400_us;
  afeConfig.sc_voltage=scd_44_22_mv;
  afeConfig.oc_delay=ocd_8_ms;
  afeConfig.oc_voltage=ocd_17_8_mv;
  afeConfig.shuntValue=5; // 5 mOhms
  afeConfig.chargeCurrent_mA = 4000;

  // voltage limit settings
  afeConfig.uv_delay=uvd_4_s;
  afeConfig.uvLimit=0xFF;
  afeConfig.ov_delay=ovd_2_s;
  afeConfig.ovLimit=0xAA;

  // generic pack configuration
  afeConfig.pack_max_voltage = 16400;
  afeConfig.cell_bal_voltage = 4100;

  if(0==bqInit(I2C1, &afeData, &afeConfig)) {
	  // ERROR
	  LL_GPIO_SetOutputPin(LED0_GPIO_Port, LED0_Pin);
	  LL_GPIO_SetOutputPin(LED1_GPIO_Port, LED1_Pin);
	  LL_GPIO_SetOutputPin(LED2_GPIO_Port, LED2_Pin);
	  asm volatile ("bkpt 0");
  } else {
	  bqEnableAll();
  }

  commInit(USART2, &afeData, &afeConfig);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1) {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  // handle AFE
	  if(LL_GPIO_IsInputPinSet(SIGNAL_GPIO_Port,SIGNAL_Pin)) {
		  LL_GPIO_SetOutputPin(LED0_GPIO_Port, LED0_Pin);

		  // AFE event signaled,
		  bqAct();
		  LL_GPIO_ResetOutputPin(LED1_GPIO_Port, LED1_Pin);
		  LL_GPIO_ResetOutputPin(LED2_GPIO_Port, LED2_Pin);
		  if(0b00001100&afeData.status_reg) { // voltage problem
			  LL_GPIO_SetOutputPin(LED1_GPIO_Port, LED1_Pin);
		  } else if(0b00000011&afeData.status_reg) { // discharge current problem
			  LL_GPIO_SetOutputPin(LED2_GPIO_Port, LED2_Pin);
		  } else if (0b11000000&afeData.status_reg) { // charge current problem
			  LL_GPIO_SetOutputPin(LED1_GPIO_Port, LED1_Pin);
			  LL_GPIO_SetOutputPin(LED2_GPIO_Port, LED2_Pin);
		  }

		  // handle the balancing system
		  bqBalance();

		  // handle the communication system
		  commAct();

		  // check the button and decide what needs to happen
		  if(LL_GPIO_IsInputPinSet(BUTTON_GPIO_Port, BUTTON_Pin)) {
			  button_debounce = 0;
		  } else {
			  if(button_debounce < BUTTON_DEBOUNCE_LIMIT) {
				  button_debounce ++;
			  } else if ( BUTTON_DEBOUNCE_LIMIT == button_debounce ) {
				  button_debounce ++;
				  // act on the button
				  if(0b11001111&afeData.status_reg) {
					  // reset error status if there's a problem
					  bqDisableAll();
					  bqResetStatus();
					  bqEnableAll();
				  } else {
					  // start the balance algo
					  afeData.balStatus=1;
				  }
			  } else {
				  button_debounce = button_debounce ;
			  }
		  }

		  LL_GPIO_ResetOutputPin(LED0_GPIO_Port, LED0_Pin);
	  }
  }
  /* USER CODE END 3 */
}

